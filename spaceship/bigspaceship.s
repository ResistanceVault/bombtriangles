DRAWBIGSPACESHIP:
  ; first bpl
  lea SCREEN_2+30,a0
  lea BIGSPACESHIP_BPL1,a1
  move.l a0,a2
  moveq #0,d5
  moveq #3-1,d7 ; Cycle for each bitplane
bigspaceship_each_bpl:
  moveq #10-1,d6 ; Cycle for each word inside each bitplane

bigspaceship_each_byte:
  move.b (a1)+,d0
  jsr DOUBLE_BYTE
  move.w d0,(a0)+
  move.b (a1)+,d0
  jsr DOUBLE_BYTE
  move.w d0,(a0)+
  ; go to next line
  adda.l #36,a0

  subq #2,a1
  move.b (a1)+,d0
  jsr DOUBLE_BYTE
  move.w d0,(a0)+
  move.b (a1)+,d0
  jsr DOUBLE_BYTE
  move.w d0,(a0)+
  ; go to next line
  adda.l #36,a0


  dbra d6,bigspaceship_each_byte

  move.l a2,a0
  addq #1,d5
  move.w d5,d6
  muls #40*224,d6
  adda.l d6,a0
  dbra d7,bigspaceship_each_bpl
  rts

